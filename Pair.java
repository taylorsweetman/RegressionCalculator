package regressioncalculator;

public class Pair {
    private final double X;
    private final double Y;

    public Pair(int x, int y) {
        this.X = (double)(x);
        this.Y = (double)(y);

    }

    public double getX() {
        return X;
    }

    public double getY() {
        return Y;
    }

}
